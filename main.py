# 2. try > operator
num1 = 5
num2 = 2
print('num1: ' + str(num1))
print('num2: ' + str(num2))
if(num1 > num2):
  print('num1 is greater!')
else: print('num1 is not greater')
print('--------------******--------------\n')

# 3. try < operator
print('num1: ' + str(num1))
print('num2: ' + str(num2))
if(num1 < num2):
  print('num1 is smaller!')
else: print('num1 is not smaller')
print('--------------******--------------\n')

# 4. try == operator
print('num1: ' + str(num1))
print('num2: ' + str(num2))
if(num1 == num2):
  print('num1 is equal to num2!')
else: print('num1 is not equal to num2')
print('--------------******--------------\n')